/**
 * Se pide confirmación para conservar los datos del tutor
 * o borrarlos
 * @return 
 */
    private void limpiar(){
       int conf = JOptionPane.showConfirmDialog(null, "Desea conservar los datos de tutor");
       if(conf==0)
           limpiarMenor();
       if(conf==1){
           limpiarMenor();
           limpiarTutor();
       }
    }

/**
 * Se limpian los campos del panel de menor
 */  
    private void limpiarMenor(){
        tfNombreM.setText(null);
        tfApelpM.setText(null);
        tfApelsM.setText(null);
        ftfDniM.setText(null);
        ftfFechaNac.setText(null);
        contador--;            
        centro = null;
        inscrip = null;
    }
/**
 * Se limpian los campos del panel de tutor
 */    
    private void limpiarTutor(){
        tfNombreT.setText(null);
        tfApelpT.setText(null);
        tfApelsT.setText(null);
        ftfDniT.setText(null);        
    }
    /**
     * Valida los datos del formulario antes de proceder a guardar los datos
     */
    private boolean validaDatos(String dniT, String dniM, String tC, String t2, String t3, String t4){
        try{
            
            validarDni(dniT);             
            validarDni(dniM);
            validarString();            
            validarTelefono(tC);
            if(!t2.equals(""))
            validarTelefono(t2);
            if(!t3.equals(""))
            validarTelefono(t3);
            if(!t4.equals(""))
            validarTelefono(t4);
            validarSexo();
            validarFechaN();
            validarDiscapacidad();
            validarCentro();
            validarModelo();
            
            return true;
        }
        catch(CampoVacio e){
            JOptionPane.showMessageDialog(this,e.getMessage());
            return false;
        }
        catch (DniNoValido e) {
            JOptionPane.showMessageDialog(this,"El dni no es correcto tiene que tener este fotmato ##.###.###-U"+ e.getMessage());
            return false;
        }
        catch(TelefonoNoValido e){
            JOptionPane.showMessageDialog(this,"El telefono no es correcto "+ e.getMessage());
            return false;
        }
        catch(FechaNoValida e){
            JOptionPane.showMessageDialog(this,"La fecha de nacimiento no es correcta"+ e.getMessage());
            return false;
        }
        catch(ModeloNoValido e){
            JOptionPane.showMessageDialog(this,"El modelo solo puede ser A,B ó C escrito en mayusculas"+ e.getMessage());
            return false;
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error en la validacion de los datos" + e.getMessage());
            return false;
        }        
    }
/**
 * Diferentes validaciones
 * @throws Exception 
 */    
    private void validarDni(String ftfdni) throws Exception{        
        String juegoCaracteres="TRWAGMYFPDXBNJZSQVHLCKET";
        String dni = ftfdni.substring(0, 2);
        dni += ftfdni.substring(3, 6);
        dni += ftfdni.substring(7, 10);
        int modulo= Integer.parseInt(dni) % 23;
        char letra = juegoCaracteres.charAt(modulo);
        if (ftfdni.charAt(11)!= letra)
                throw new DniNoValido();
    }
    
    private void validarString() throws Exception{
        if (ftfDniT.getValue()== null)
                throw new CampoVacio("Dni");
        if (tfNombreM.getText().isEmpty())
                throw new CampoVacio("Nombre");
        if (tfNombreT.getText().isEmpty())
                throw new CampoVacio("Nombre");
        if (tfApelpT.getText().isEmpty())
                throw new CampoVacio("Apellido");
        if (tfApelsT.getText().isEmpty())
                throw new CampoVacio("Apellido");
        if (tfApelpM.getText().isEmpty())
                throw new CampoVacio("Apellido");
        if (tfApelsM.getText().isEmpty())
                throw new CampoVacio("Apellido");
        if (taDireccion.getText().isEmpty())
                throw new CampoVacio("Direccion");
        if (tfTelfContacto.getText().isEmpty())
                throw new CampoVacio("Telefono");
    }
    
    private void validarTelefono(String telf) throws Exception{
        Pattern pat = Pattern.compile("^[6,7,8]{1}([0-9]{8}||[0-9]{12})$");
        Matcher mat = pat.matcher(telf);
        if (mat.matches()==false) 
           throw new TelefonoNoValido();
    }
    
    private void validarSexo() throws Exception{
        if (rbHombre.isSelected() == false && rbMujer.isSelected() == false)
            throw new CampoVacio("Sexo");
    }
    
    private void validarFechaN() throws Exception{
        if (ftfFechaNac.getValue()== null)
                throw new CampoVacio("FechaN");
    }
    
    private void validarDiscapacidad() throws Exception{ 
        if (chbDiscapacidad.getIgnoreRepaint())
                throw new CampoVacio("Discapacidad");
    }
    
    private void validarCentro() throws Exception{
        if (cbCentroEd.getIgnoreRepaint())
                throw new CampoVacio("Centro");
    }
    
    private void validarModelo() throws Exception{
        if (ftfModelo.getValue()== null)
                throw new CampoVacio("Modelo");
        Pattern pat = Pattern.compile("^[ABC]{1}$");
        Matcher mat = pat.matcher(ftfModelo.getText());
        if (mat.matches()==false) 
           throw new ModeloNoValido();
    }

/**
 * Se cuentan los participantes insertados 
 * para deshabilitar el boton de añadir otro
 */    
    private void compruebaCantidadInscripciones(){               
        if(contador==2){
          bParticipante.setEnabled(false); 
        }            
        else contador =  contador+1;
    }
    
  
    private void llenarCbCentros(ArrayList<CentroEd> listado)
    {
        cbCentroEd.removeAllItems();
        for (int i = 0; i < listado.size(); i++)
        {
            this.cbCentroEd.addItem(listado.get(i).getNombreCtr());          
        }
    }
    
 
