    /**
     * Guarda los datos en un objeto inscripcion para poder pasar a la siguiente si la hubiese
     */
    private void guardaDatosInscripcion()
    {
        //valida los datos del formulario 
        if(validaDatos(ftfDniM.getText(),ftfDniT.getText(),tfTelfContacto.getText(),tfTelf2.getText(),tfTelf3.getText(),tfTelf4.getText()))
        {   
            //Si es correcto procede a guardar datos
            //crea inscripcion
            inscrip = Control.creaInscripcion();
            //anade id inscripcion
            inscrip.setIdIns(contador);
            contador ++;
            //anade solicitud a inscripcion, y ademas anade inscripcion en solicitud.inscripciones(arrayList)
            inscrip.setSolicitudBidireccional(sol);
            //anade direccion
            inscrip.setDireccion(direccion);
            //si tutor no existe
            Tutor t = Control.buscaTutor(this.ftfDniT.toString());
            if(t==null)
            {
                //anade tutor pasandole los datos del formulario
                inscrip.setTutor(
                        this.ftfDniT.getText(),
                        this.tfNombreT.getText(),
                        this.tfApelpT.getText(),
                        this.tfApelsT.getText()
                );
            //si tutor existe
            }else
            {
                //anade el tutor buscado anteriormente
                inscrip.setTutor(t);
            }
            
            //averigua el sexo
            char sexo;
            if(rbHombre.isSelected())
                sexo = 'H';
            else
                sexo = 'M';
            //gurada los datos anadiendo un menor a la inscripcion
            inscrip.setMenor(
                    this.ftfDniM.getText(),
                    this.tfNombreM.getText(),
                    this.tfApelpM.getText(),
                    this.tfApelsM.getText(),
                    sexo,
                    this.ftfFechaNac.getText(),
                    this.chbDiscapacidad.isSelected(),
                    this.ftfModelo.getText().charAt(0),
                    centro                
            );
            //anade los telefonos
            inscrip.setTelf1(tfTelfContacto.getText());
            inscrip.setTelf2(tfTelf2.getText());
            inscrip.setTelf3(tfTelf3.getText());
            inscrip.setTelf4(tfTelf4.getText());
            //inscripcion ya esta completa
        }
        
        //se comprueba si el menor ya esta inscrito en otra solicitud de la BD
        //NOTA!! falta comprobar si esta repetido en la solicitud en curso
        if(Control.existeMenor(inscrip.getMenor()))
        {
            //si que lo esta
            //mensaje de error
            JOptionPane.showMessageDialog
            (
                this, 
                inscrip.getMenor().getNombre() +
                " " +
                inscrip.getMenor().getApel1() +
                " " +
                inscrip.getMenor().getApel2() +
                " ya esta inscrito en otra solicitud.\n" +
                "¡NO PUEDE APUNTAR DOS VECES A LA MISMA PERSONA!"
            );            
            //se borran los datos de menor          
            this.limpiarMenor();
        }
        //si no esta repetido se continua con el proceso
    }  
