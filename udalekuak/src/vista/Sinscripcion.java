/**
 * Se pide confirmaci贸n para conservar los datos del tutor
 * o borrarlos
 * @return 
 */
    private void limpiar(){
       int conf = JOptionPane.showConfirmDialog(null, "Desea conservar los datos de tutor");
       if(conf==0)
           limpiarMenor();
       if(conf==1){
           limpiarMenor();
           limpiarTutor();
       }
    }

